package io.gravitee.apim.archunit;

import static java.util.Collections.emptyList;

import io.gravitee.node.archunit.LoggingArchitectureRules;
import java.util.Set;
import org.junit.jupiter.api.DisplayNameGeneration;
import org.junit.jupiter.api.DisplayNameGenerator;
import org.junit.jupiter.api.Test;

@DisplayNameGeneration(DisplayNameGenerator.ReplaceUnderscores.class)
public class LoggingArchitectureTest {

    public static final String[] GRAVITEE_APIM_PACKAGES_TO_CHECK = {
        "io.gravitee.gateway..",
        "io.gravitee.definition..",
        "io.gravitee.repository..",
        "io.gravitee.rest.api..",
        "io.gravitee.apim..",
        "io.gravitee.plugin.apiservice..",
        "io.gravitee.plugin.entrypoint..",
        "io.gravitee.plugin.endpoint..",
    };

    public static final String[] PACKAGES_EXCLUDED_FROM_CHECK = { "io.gravitee.gateway.reactive.api..", "io.gravitee.gateway.api.." };

    private static final Set<String> ALLOW_LIST = Set.of(
        //TODO: *ConfigurationEvaluator classes are generated by gravitee-plugin-annotation-processors. While this lib is not migrated, we should exclude those classes explicitely
        "io.gravitee.plugin.endpoint.http.proxy.configuration.HttpProxyEndpointConnectorConfigurationEvaluator",
        "io.gravitee.plugin.endpoint.http.proxy.configuration.HttpProxyEndpointConnectorSharedConfigurationEvaluator",
        "io.gravitee.plugin.endpoint.tcp.proxy.configuration.TcpProxyEndpointConnectorSharedConfigurationEvaluator"
    );

    @Test
    public void should_not_use_slf4j_logger_factory() {
        LoggingArchitectureRules.configure()
            .resideInAnyPackage(GRAVITEE_APIM_PACKAGES_TO_CHECK)
            .excludePackagesFromScan(PACKAGES_EXCLUDED_FROM_CHECK)
            .allowIn(ALLOW_LIST)
            .checkNoSlf4jLoggerFactory();
    }
}
