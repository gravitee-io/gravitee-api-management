import {
    Button,
    Card,
    DataTablePagination,
    DateRangePicker,
    Empty,
    EmptyContent,
    EmptyDescription,
    EmptyHeader,
    EmptyTitle,
    Select,
    SelectContent,
    SelectItem,
    SelectTrigger,
    SelectValue,
    Skeleton,
    Table,
    TableBody,
    TableCell,
    TableHead,
    TableHeader,
    TableRow,
    Tooltip,
    TooltipContent,
    TooltipProvider,
    TooltipTrigger,
} from '@gravitee/graphene-core';
import { EyeIcon, XIcon } from '@gravitee/graphene-core/icons';
import { Fragment, useCallback, useMemo, useState } from 'react';
import type { DateRange } from 'react-day-picker';
import { useParams } from 'react-router-dom';

import { AuditLogsLanding } from '../features/apis/components/detail/audit/AuditLogsLanding';
import { formatDate } from '../features/apis/components/detail/deployment/utils';
import { useAuditEvents } from '../features/apis/hooks/useAuditEvents';
import { useAuditLogs } from '../features/apis/hooks/useAuditLogs';
import type { Audit } from '../features/apis/types/auditLogs.types';

const PAGE_SIZE_OPTIONS = [10, 20, 25, 50, 100];
const ALL_EVENTS = '__all__';

function formatPatch(patch: string): string {
    try {
        return JSON.stringify(JSON.parse(patch), null, 2);
    } catch {
        return patch;
    }
}

function formatTarget(audit: Audit): string {
    return audit.properties.map(p => `${p.key}: ${p.value}`).join('\n');
}

export function AuditLogsPage() {
    const { apiId } = useParams<{ apiId: string }>();

    const [page, setPage] = useState(1);
    const [perPage, setPerPage] = useState(10);
    const [selectedEvent, setSelectedEvent] = useState('');
    const [dateRange, setDateRange] = useState<DateRange | undefined>();
    const [expandedId, setExpandedId] = useState<string | null>(null);

    const from = dateRange?.from ? dateRange.from.getTime() : undefined;
    const to = dateRange?.to ? dateRange.to.getTime() : undefined;

    const params = useMemo(
        () => ({ page, perPage, events: selectedEvent || undefined, from, to }),
        [page, perPage, selectedEvent, from, to],
    );

    const { data, isLoading } = useAuditLogs(apiId, params);
    const { data: eventTypes } = useAuditEvents(apiId);

    const audits = data?.data ?? [];
    const totalCount = data?.pagination.totalCount ?? 0;
    const hasFilters = Boolean(selectedEvent || dateRange);

    const handleEventChange = useCallback((value: string) => {
        setSelectedEvent(value === ALL_EVENTS ? '' : value);
        setPage(1);
    }, []);

    const handleDateChange = useCallback((range: DateRange | undefined) => {
        setDateRange(range);
        setPage(1);
    }, []);

    const handleReset = useCallback(() => {
        setSelectedEvent('');
        setDateRange(undefined);
        setPage(1);
    }, []);

    const handlePageSizeChange = useCallback((size: number) => {
        setPerPage(size);
        setPage(1);
    }, []);

    const toggleExpand = useCallback((id: string) => {
        setExpandedId(prev => (prev === id ? null : id));
    }, []);

    if (!isLoading && !hasFilters && totalCount === 0) {
        return <AuditLogsLanding />;
    }

    return (
        <TooltipProvider>
            <div className="space-y-4 p-6">
                <div className="space-y-1">
                    <h1 className="text-2xl font-semibold tracking-tight">Audit Logs</h1>
                    <p className="text-sm text-muted-foreground">Gather all events and changes generated by the API Management</p>
                </div>

                {/* Single toolbar row: filters on the left, pagination on the right */}
                <div className="flex items-center justify-between gap-3">
                    <div className="flex items-center gap-2 flex-wrap">
                        <Select value={selectedEvent || ALL_EVENTS} onValueChange={handleEventChange}>
                            <SelectTrigger className="w-52" aria-label="Filter by event type">
                                <SelectValue placeholder="All events" />
                            </SelectTrigger>
                            <SelectContent position="popper" className="max-h-72 overflow-y-auto">
                                <SelectItem value={ALL_EVENTS}>All events</SelectItem>
                                {(eventTypes ?? []).map(event => (
                                    <SelectItem key={event} value={event}>
                                        {event}
                                    </SelectItem>
                                ))}
                            </SelectContent>
                        </Select>
                        <DateRangePicker
                            value={dateRange}
                            onChange={handleDateChange}
                            placeholder="Date range"
                            numberOfMonths={2}
                            aria-label="Filter by date range"
                        />
                        {hasFilters ? (
                            <Button type="button" variant="ghost" size="sm" onClick={handleReset} className="gap-1">
                                <XIcon className="size-3.5" aria-hidden="true" />
                                Reset
                            </Button>
                        ) : null}
                    </div>
                    {!isLoading && totalCount > 0 ? (
                        <DataTablePagination
                            page={page}
                            pageSize={perPage}
                            totalCount={totalCount}
                            pageSizeOptions={PAGE_SIZE_OPTIONS}
                            onPageChange={setPage}
                            onPageSizeChange={handlePageSizeChange}
                        />
                    ) : null}
                </div>

                {isLoading ? (
                    <div className="space-y-2">
                        {Array.from({ length: 8 }).map((_, i) => (
                            <Skeleton key={i} className="h-12 rounded-lg" />
                        ))}
                    </div>
                ) : audits.length === 0 ? (
                    <Card>
                        <Empty>
                            <EmptyHeader>
                                <EmptyTitle>No audit logs found</EmptyTitle>
                                <EmptyDescription>Try adjusting or clearing your filters.</EmptyDescription>
                            </EmptyHeader>
                            <EmptyContent />
                        </Empty>
                    </Card>
                ) : (
                    <div className="rounded-lg border overflow-hidden">
                        <Table>
                            <TableHeader>
                                <TableRow>
                                    <TableHead className="w-44">Date</TableHead>
                                    <TableHead className="w-40">Actor</TableHead>
                                    <TableHead className="w-52">Event</TableHead>
                                    <TableHead>Target</TableHead>
                                    <TableHead className="w-16 text-right pr-4">Patch</TableHead>
                                </TableRow>
                            </TableHeader>
                            <TableBody>
                                {audits.map(audit => {
                                    const isExpanded = expandedId === audit.id;
                                    const hasPatch = Boolean(audit.patch);
                                    const target = formatTarget(audit);

                                    return (
                                        <Fragment key={audit.id}>
                                            <TableRow className={isExpanded ? 'bg-muted/40' : undefined}>
                                                <TableCell className="whitespace-nowrap text-sm">{formatDate(audit.createdAt)}</TableCell>
                                                <TableCell className="text-sm">{audit.user.displayName}</TableCell>
                                                <TableCell className="font-mono text-sm">{audit.event}</TableCell>
                                                <TableCell className="text-sm">
                                                    {target ? (
                                                        target.split('\n').map(line => <div key={line}>{line}</div>)
                                                    ) : (
                                                        <span className="text-muted-foreground/40 italic">—</span>
                                                    )}
                                                </TableCell>
                                                <TableCell className="text-right pr-4">
                                                    {hasPatch ? (
                                                        <Tooltip>
                                                            <TooltipTrigger asChild>
                                                                <Button
                                                                    type="button"
                                                                    variant="ghost"
                                                                    size="icon"
                                                                    className="size-8"
                                                                    onClick={() => toggleExpand(audit.id)}
                                                                    aria-label={isExpanded ? 'Close patch view' : 'View patch'}
                                                                >
                                                                    {isExpanded ? (
                                                                        <XIcon className="size-4" />
                                                                    ) : (
                                                                        <EyeIcon className="size-4" />
                                                                    )}
                                                                </Button>
                                                            </TooltipTrigger>
                                                            <TooltipContent>{isExpanded ? 'Close' : 'View patch'}</TooltipContent>
                                                        </Tooltip>
                                                    ) : null}
                                                </TableCell>
                                            </TableRow>

                                            {isExpanded ? (
                                                <TableRow>
                                                    <TableCell colSpan={5} className="p-0">
                                                        <PatchPanel patch={audit.patch} />
                                                    </TableCell>
                                                </TableRow>
                                            ) : null}
                                        </Fragment>
                                    );
                                })}
                            </TableBody>
                        </Table>
                    </div>
                )}

                {/* Bottom pagination — only shown when there are results */}
                {!isLoading && totalCount > 0 ? (
                    <div className="flex justify-end">
                        <DataTablePagination
                            page={page}
                            pageSize={perPage}
                            totalCount={totalCount}
                            pageSizeOptions={PAGE_SIZE_OPTIONS}
                            onPageChange={setPage}
                            onPageSizeChange={handlePageSizeChange}
                        />
                    </div>
                ) : null}
            </div>
        </TooltipProvider>
    );
}

function PatchPanel({ patch }: Readonly<{ patch: string }>) {
    return (
        <div className="border-t bg-muted/20 px-6 py-4">
            <p className="mb-2 text-xs font-semibold text-muted-foreground">JSON Patch</p>
            <pre
                className="overflow-auto rounded-lg bg-muted p-4 text-xs font-mono text-foreground"
                style={{ maxHeight: '240px', whiteSpace: 'pre-wrap', wordBreak: 'break-word' }}
            >
                {formatPatch(patch)}
            </pre>
        </div>
    );
}
