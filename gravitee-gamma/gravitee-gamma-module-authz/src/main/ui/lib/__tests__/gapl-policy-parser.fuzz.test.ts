/**
 * Property-based fuzz tests for `parseGaplToStatements` — the GAPL → visual
 * roundtrip parser used by the visual policy editor.
 *
 * These complement the example-based tests in `gapl-policy-parser.test.ts`:
 *
 *   1. Roundtrip: `parseGaplToStatements(statementsToGapl(...))` is shape-
 *      equivalent to the input statements (modulo regenerated statement ids).
 *   2. Slot-only: bare `permit (principal, action, resource)` always parses to
 *      a single statement with empty refs.
 *   3. Multi-statement order: a sequence of `permit`/`forbid` keywords is
 *      preserved in the parsed `effect` field.
 *   4. Comments + whitespace: random `// comment` lines inserted between
 *      statements do not change the parsed result.
 *   5. Effect alternation: any random sequence of `permit`/`forbid` round-
 *      trips through the parser.
 *   6. Unsupported syntax: random strings starting with `unless` (or other
 *      unsupported keywords) return `null` instead of throwing.
 *
 * `numRuns` is kept to 50–80 so the suite stays well under a second on CI.
 */
import * as fc from 'fast-check';
import { describe, expect, it } from 'vitest';
import {
    statementToGapl,
    statementsToGapl,
    type ActionRef,
    type PolicyEffect,
    type PolicyStatement,
    type PrincipalRef,
    type ResourceRef,
} from '../../app/features/policy-management/statement-to-gapl';
import { parseGaplToStatements } from '../gapl-policy-parser';

const RUNS = 60;

// ---------------------------------------------------------------------------
// Generators — match the parser's actual grammar, not arbitrary garbage.
// ---------------------------------------------------------------------------

// Type tokens like `User`, `Group`, `Tool` — start with an alpha, [A-Za-z0-9_]
// only. Keep names short to avoid bloating round-trip strings.
const typeTok = fc
    .string({ minLength: 1, maxLength: 8, unit: 'binary-ascii' })
    .filter(s => /^[A-Z][A-Za-z0-9_]*$/.test(s));

// Label/id tokens — printable ASCII without `"` or `\` (the serialiser would
// have to escape those, which complicates round-trip equivalence checking and
// isn't what we're testing here).
const labelTok = fc
    .string({ minLength: 1, maxLength: 10, unit: 'binary-ascii' })
    .filter(s => /^[A-Za-z0-9_.\-]+$/.test(s));

const principalRef: fc.Arbitrary<PrincipalRef> = fc.record({
    id: labelTok,
    kind: typeTok,
    label: labelTok,
});

const actionRef: fc.Arbitrary<ActionRef> = fc.record({
    id: labelTok,
    kind: fc.constant('Action'), // serialiser default; keeps round-trip stable
    label: labelTok,
});

const resourceRef: fc.Arbitrary<ResourceRef> = fc.record({
    id: labelTok,
    kind: typeTok,
    label: labelTok,
});

const effect: fc.Arbitrary<PolicyEffect> = fc.constantFrom('permit', 'forbid');

const policyStatement: fc.Arbitrary<PolicyStatement> = fc.record({
    id: fc.constant('s'), // ids are regenerated by the parser; value irrelevant
    effect,
    principals: fc.array(principalRef, { maxLength: 3 }),
    actions: fc.array(actionRef, { maxLength: 3 }),
    resources: fc.array(resourceRef, { maxLength: 3 }),
    condition: fc.constant(''), // condition shapes are exercised in example tests
});

/** Strip volatile fields so we can compare statements by content. */
function shape(s: PolicyStatement) {
    return {
        effect: s.effect,
        principals: s.principals.map(p => ({ kind: p.kind, label: p.label })),
        actions: s.actions.map(a => ({ label: a.label })),
        resources: s.resources.map(r => ({ kind: r.kind, label: r.label })),
        condition: (s.condition ?? '').trim(),
    };
}

// ---------------------------------------------------------------------------
// Property 1 — Roundtrip via statementsToGapl
// ---------------------------------------------------------------------------

describe('parseGaplToStatements — fuzz: roundtrip via statementsToGapl', () => {
    it('parses serialised statements back to the same shapes', () => {
        fc.assert(
            fc.property(fc.array(policyStatement, { minLength: 1, maxLength: 4 }), stmts => {
                const text = statementsToGapl('p', stmts, { label: 'Test' });
                const parsed = parseGaplToStatements(text);
                expect(parsed).not.toBeNull();
                expect(parsed!.statements).toHaveLength(stmts.length);
                expect(parsed!.statements.map(shape)).toEqual(stmts.map(shape));
            }),
            { numRuns: RUNS },
        );
    });
});

// ---------------------------------------------------------------------------
// Property 2 — Slot-only roundtrip
// ---------------------------------------------------------------------------

describe('parseGaplToStatements — fuzz: slot-only form', () => {
    it('always parses bare `<effect> (principal, action, resource);` to one empty statement', () => {
        fc.assert(
            fc.property(effect, eff => {
                const text = `${eff} (principal, action, resource);`;
                const parsed = parseGaplToStatements(text);
                expect(parsed).not.toBeNull();
                expect(parsed!.statements).toHaveLength(1);
                const s = parsed!.statements[0];
                expect(s.effect).toBe(eff);
                expect(s.principals).toEqual([]);
                expect(s.actions).toEqual([]);
                expect(s.resources).toEqual([]);
            }),
            { numRuns: RUNS },
        );
    });
});

// ---------------------------------------------------------------------------
// Property 3 — Multi-statement order is preserved
// ---------------------------------------------------------------------------

describe('parseGaplToStatements — fuzz: multi-statement order preservation', () => {
    it('preserves the order of statements in the source', () => {
        fc.assert(
            fc.property(fc.array(effect, { minLength: 2, maxLength: 5 }), effects => {
                const text = effects.map(e => `${e} (principal, action, resource);`).join('\n');
                const parsed = parseGaplToStatements(text);
                expect(parsed).not.toBeNull();
                expect(parsed!.statements.map(s => s.effect)).toEqual(effects);
            }),
            { numRuns: RUNS },
        );
    });
});

// ---------------------------------------------------------------------------
// Property 4 — `// comment` lines and blank lines are transparent
// ---------------------------------------------------------------------------

describe('parseGaplToStatements — fuzz: comments + blank lines are ignored', () => {
    // Comment bodies are restricted to safe printable ASCII (no quotes, no
    // control chars, no newlines) — the comment-stripper trims to end-of-
    // line only on visible `//`, but smuggling `\n` or `\r` would change line
    // shape in ways we're not testing here.
    const commentLine = fc
        .string({ minLength: 0, maxLength: 30, unit: 'binary-ascii' })
        .filter(s => /^[ A-Za-z0-9_.\-:,()]*$/.test(s))
        .map(s => `// ${s}`);

    it('produces the same parse result with random `// ...` lines interleaved', () => {
        fc.assert(
            fc.property(
                fc.array(policyStatement, { minLength: 1, maxLength: 3 }),
                fc.array(commentLine, { maxLength: 4 }),
                (stmts, comments) => {
                    const tight = stmts.map(statementToGapl).join('\n\n');
                    // Sprinkle the comment lines around the statements.
                    const loose = [...comments, ...stmts.map(statementToGapl), ...comments].join('\n');

                    const a = parseGaplToStatements(tight);
                    const b = parseGaplToStatements(loose);

                    expect(a).not.toBeNull();
                    expect(b).not.toBeNull();
                    expect(b!.statements.map(shape)).toEqual(a!.statements.map(shape));
                },
            ),
            { numRuns: RUNS },
        );
    });
});

// ---------------------------------------------------------------------------
// Property 5 — Effect keyword alternation is preserved
// ---------------------------------------------------------------------------

describe('parseGaplToStatements — fuzz: effect alternation preserved', () => {
    it('round-trips arbitrary permit/forbid sequences with a single principal each', () => {
        fc.assert(
            fc.property(fc.array(effect, { minLength: 1, maxLength: 6 }), effects => {
                const text = effects
                    .map(e => `${e} (principal == User::"u", action == Action::"a", resource == Tool::"t");`)
                    .join('\n');
                const parsed = parseGaplToStatements(text);
                expect(parsed).not.toBeNull();
                expect(parsed!.statements.map(s => s.effect)).toEqual(effects);
            }),
            { numRuns: RUNS },
        );
    });
});

// ---------------------------------------------------------------------------
// Property 6 — Unsupported syntax returns null (never throws)
// ---------------------------------------------------------------------------

describe('parseGaplToStatements — fuzz: unsupported syntax returns null', () => {
    // Keywords the parser does not (and should not) accept at top level.
    const unsupportedKeyword = fc.constantFrom('unless', 'allow', 'deny', 'rule', 'when', 'else');

    it('returns null for inputs that begin with an unsupported top-level keyword', () => {
        fc.assert(
            fc.property(unsupportedKeyword, kw => {
                // Build syntactically plausible-looking but unsupported input.
                const text = `${kw} (principal == User::"a", action == Action::"r", resource == Tool::"t");`;
                expect(() => parseGaplToStatements(text)).not.toThrow();
                expect(parseGaplToStatements(text)).toBeNull();
            }),
            { numRuns: 50 },
        );
    });

    it('returns null for `unless { ... }` clauses appended after a valid statement', () => {
        fc.assert(
            fc.property(typeTok, labelTok, (t, l) => {
                const text = `permit (principal == ${t}::"${l}", action == Action::"r", resource == Tool::"t") unless { false };`;
                expect(parseGaplToStatements(text)).toBeNull();
            }),
            { numRuns: 50 },
        );
    });
});
